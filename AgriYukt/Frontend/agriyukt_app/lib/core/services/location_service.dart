  // --- Getters ---
  static List<LocalizedItem> getStates() {
    return _data.keys.map((k) => LocalizedItem(k, k, k)).toList();
  }

  static List<LocalizedItem> getDistricts(String state) {
    if (!_data.containsKey(state)) return [];
    return _data[state]!.keys.map((k) => LocalizedItem(k, k, k)).toList();
  }

  static List<LocalizedItem> getTalukas(String state, String district) {
    if (!_data.containsKey(state) || !_data[state]!.containsKey(district))
      return [];
    return _data[state]![district]!
        .keys
        .map((k) => LocalizedItem(k, k, k))
        .toList();
  }

  static List<LocalizedItem> getVillages(
      String state, String district, String taluka) {
    if (!_data.containsKey(state) ||
        !_data[state]!.containsKey(district) ||
        !_data[state]![district]!.containsKey(taluka)) return [];
    return _data[state]![district]![taluka]!
        .map((k) => LocalizedItem(k, k, k))
        .toList();
  }

